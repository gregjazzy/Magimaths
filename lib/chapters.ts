export const getChaptersByClass = (classLevel: ClassLevel) => {
  return chaptersData.filter(chapter => 
    chapter.classLevel === classLevel && !hiddenChapters.includes(chapter.id)
  );
};

export const getChaptersByCategory = (category: string) => {
  return chaptersData.filter(chapter => chapter.category === category);
};

export const getChaptersByClassAndCategory = (classLevel: ClassLevel, category: string) => {
  return chaptersData.filter(chapter => 
    chapter.classLevel === classLevel && 
    chapter.category === category &&
    !hiddenChapters.includes(chapter.id)
  );
};

export const getChapterById = (id: string) => {
  return chaptersData.find(chapter => chapter.id === id);
};

export const getAvailableClassLevels = (): ClassLevel[] => {
  const levels = Array.from(new Set(chaptersData.map(chapter => chapter.classLevel)));
  return levels as ClassLevel[];
};

export const getChaptersGroupedByClass = () => {
  const grouped: { [key in ClassLevel]?: Chapter[] } = {};
  
  chaptersData.forEach(chapter => {
    // Exclure les chapitres cach√©s de l'affichage
    if (hiddenChapters.includes(chapter.id)) {
      return;
    }
    
    if (!grouped[chapter.classLevel]) {
      grouped[chapter.classLevel] = [];
    }
    grouped[chapter.classLevel]!.push(chapter);
  });
  
  return grouped;
};

export const getSubChapters = (parentChapterId: string) => {
  return chaptersData.filter(chapter => chapter.parentChapter === parentChapterId);
}; 